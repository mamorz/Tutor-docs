package edu.kit.kastel.logistics;

/**
 * Enum representing possible data format to expect/handle while user is in debugging process of the second phase of {@link Competition}.
 * @author upgcv
 */
public enum DebugType {

    /**
     * A Boolean is to expect/handle.
     */
    BOOLEAN,

    /**
     * An Integer is to expect/handle.
     */
    INTEGER,

    /**
     * A Double is to expect/handle.
     */
    DOUBLE
}
